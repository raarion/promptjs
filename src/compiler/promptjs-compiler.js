PromptJSCompiler.prototype.lowerExpression = function (node) {
  return ExpressionLowering.lowerExpression(this, node);
};

// Install statement visitor emitters after core compiler helpers are defined.
StatementEmitters.install(PromptJSCompiler, accept);

module.exports = PromptJSCompiler;
